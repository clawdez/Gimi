let items = [...COMMUNITY_ITEMS];

export function getItems(): RentalItem[] {
  return items;
}

export function getItem(id: string): RentalItem | undefined {
  return items.find((item) => item.id === id);
}

export function startRental(id: string, renter: string, hours: number): RentalItem | undefined {
  const now = Date.now();
  let updated: RentalItem | undefined;
  items = items.map((item) => {
    if (item.id !== id) return item;
    updated = {
      ...item,
      status: "rented",
      renter,
      rentalStart: now,
      rentalHours: hours,
    };
    return updated;
  });
  return updated;
}

export function requestReturn(id: string): RentalItem | undefined {
  let updated: RentalItem | undefined;
  items = items.map((item) => {
    if (item.id !== id) return item;
    updated = { ...item, status: "return_requested" };
    return updated;
  });
  return updated;
}

export function createReturnedReceipt(item: RentalItem): RentalReceipt {
  const hours = item.rentalStart ? Math.max(1, Math.ceil((Date.now() - item.rentalStart) / 3600000)) : item.expectedHours;
  const grossFee = Math.min(Math.max(item.minimumFee, hours * item.ratePerHour), item.buyoutCap);
  const platformFee = Number((grossFee * 0.05).toFixed(2));
  return {
    id: `receipt_${crypto.randomUUID().slice(0, 8)}`,
    sessionId: `session_${item.id}`,
    itemId: item.id,
    outcome: "returned_ok",
    grossFee,
    platformFee,
    ownerPayout: Number((grossFee - platformFee).toFixed(2)),
    renterRefund: Number((item.buyoutCap - grossFee).toFixed(2)),
    rentalTokenStatus: "burned",
    createdAt: Date.now(),
  };
}
